-- Run this once in your Neon SQL editor to set up the schema

CREATE TABLE IF NOT EXISTS logs (
  id        SERIAL PRIMARY KEY,
  user_id   VARCHAR(10)   NOT NULL,
  date      DATE          NOT NULL,
  weight    DECIMAL(5,1),
  body_fat  DECIMAL(4,1),
  calories  INTEGER       NOT NULL DEFAULT 0,
  protein   INTEGER       NOT NULL DEFAULT 0,
  water     INTEGER       NOT NULL DEFAULT 0,
  sodium    INTEGER       NOT NULL DEFAULT 0,
  potassium INTEGER       NOT NULL DEFAULT 0,
  magnesium INTEGER       NOT NULL DEFAULT 0,
  creatine  BOOLEAN       NOT NULL DEFAULT FALSE,
  zepbound  BOOLEAN       NOT NULL DEFAULT FALSE,
  UNIQUE(user_id, date)
);

CREATE TABLE IF NOT EXISTS workouts (
  id        SERIAL PRIMARY KEY,
  user_id   VARCHAR(10)   NOT NULL,
  date      DATE          NOT NULL,
  type      VARCHAR(20)   NOT NULL,
  phase     VARCHAR(20),
  exercises JSONB         NOT NULL DEFAULT '[]',
  UNIQUE(user_id, date)
);

CREATE TABLE IF NOT EXISTS settings (
  user_id           VARCHAR(10) PRIMARY KEY,
  workout_start_date DATE
);

